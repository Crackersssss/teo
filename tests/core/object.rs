use tokio::test;


#[tokio::test]
async fn it_works() {
    let result = 2 + 2;
    assert_eq!(result, 4);
}

#[tokio::test]
async fn it_also_works() {
    let result = 2 + 2;
    assert_eq!(result, 4);
}

#[tokio::test]
async fn it_still_works() {
    let result = 2 + 2;
    assert_eq!(result, 4);
}
